create schema if not exists oresdb;
create extension if not exists btree_gist;
set schema 'oresdb';

--
-- generated by sqlgen and manually adapted for temporal support.
--
create table if not exists "oresdb"."accounts" (
    "id" uuid not null,
    "version" integer not null,
    "username" text not null,
    "password_hash" text not null,
    "password_salt" text not null,
    "totp_secret" text not null,
    "email" text not null,
    "is_admin" boolean not null,
    "modified_by" text not null,
    "valid_from" timestamp with time zone not null,
    "valid_to" timestamp with time zone not null,
    primary key (id, valid_from, valid_to),
    exclude using gist (
        id WITH =,
        tstzrange(valid_from, valid_to) WITH &&
    )
);

create unique index if not exists accounts_username_unique_idx
on "oresdb"."accounts" (username)
where valid_to = '9999-12-31 23:59:59'::timestamptz;

create unique index if not exists accounts_email_unique_idx
on "oresdb"."accounts" (email)
where valid_to = '9999-12-31 23:59:59'::timestamptz;

create or replace function update_accounts()
returns trigger as $$
begin
    update "oresdb"."accounts"
    set valid_to = current_timestamp
    where id = new.id
    and valid_to = '9999-12-31 23:59:59'::timestamptz
    and valid_from < current_timestamp;

    new.valid_from = current_timestamp;
    new.valid_to = '9999-12-31 23:59:59'::timestamptz;
    new.modified_by = current_user;

    return new;
end;
$$ language plpgsql;

create or replace trigger update_accounts_trigger
before insert on "oresdb"."accounts"
for each row
execute function update_accounts();
