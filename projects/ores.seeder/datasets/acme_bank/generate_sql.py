"""
Generates populate SQL for the Acme Bank dataset from the JSON files
produced by generate_data.py in this same directory.

Mirrors the shape/conventions of hand-written ores.sql populate scripts
(see projects/ores.sql/populate/testdata/testdata_business_units_artefact_populate.sql
for the pattern this follows): a dataset-registration script plus one
artefact-populate script per (dataset, artefact family), each wrapped in a
DO $$ ... $$ block that looks up its dataset_id, clears existing rows for
idempotency, and inserts.

lei_entities/lei_relationships describe the whole 4-entity group in one
shared dataset each (matching how GLEIF's own datasets work). Business
units/portfolios/books/accounts/account_contact_informations get one
dataset PER LEGAL ENTITY (not shared): existing publish-from-dq functions
are single-party-scoped per call, so each entity needs its own
independently-publishable dataset -- see the parent story's "Four legal
entities" decision.

Usage:
    python3 generate_sql.py
"""

