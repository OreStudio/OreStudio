#ifndef ORES_HTTP_DOMAIN_HTTP_STATUS_HPP
#define ORES_HTTP_DOMAIN_HTTP_STATUS_HPP

#include <cstdint>

namespace ores::http::domain {

/**
 * @brief Common HTTP status codes.
 */
enum class http_status : std::uint16_t {
    ok = 200,
    created = 201,
    no_content = 204,
    bad_request = 400,
    unauthorized = 401,
    forbidden = 403,
    not_found = 404,
    method_not_allowed = 405,
    internal_server_error = 500,
    not_implemented = 501,
    service_unavailable = 503
};

}

#endif
