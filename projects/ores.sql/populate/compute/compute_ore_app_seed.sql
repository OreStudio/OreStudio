/**
 * ORE Application Seed Script
 *
 * Seeds the ORE (Open Source Risk Engine) application and its standard app
 * version in the compute grid. Idempotent: guarded by name/version lookups
 * so it is safe to run on every database recreation. UUIDs are generated by
 * PostgreSQL on first insert and looked up on subsequent runs.
 *
 * The system tenant (ffffffff-ffff-ffff-ffff-ffffffffffff) owns these
 * records so they are visible to all tenants through RLS.
 */

\echo '--- ORE Application Seed ---'

-- -------------------------------------------------------------------------
-- ORE Application
-- -------------------------------------------------------------------------
insert into ores_compute_apps_tbl (
    id,
    tenant_id,
    version,
    name,
    description,
    modified_by,
    performed_by,
    change_reason_code,
    change_commentary,
    valid_from,
    valid_to
)
select
    gen_random_uuid(),
    ores_iam_system_tenant_id_fn(),
    1,
    'ORE',
    'Open Source Risk Engine — QuantLib-based risk analytics engine for '
    'market risk, counterparty credit risk, XVA, and sensitivity analysis.',
    'system',
    'system',
    'system.new_record',
    '',
    current_timestamp,
    ores_utility_infinity_timestamp_fn()
where not exists (
    select 1 from ores_compute_apps_tbl
    where name = 'ORE'
      and tenant_id = ores_iam_system_tenant_id_fn()
      and valid_to = ores_utility_infinity_timestamp_fn()
);

-- -------------------------------------------------------------------------
-- ORE App Version 9.0.0 (linux-x86_64)
-- -------------------------------------------------------------------------
with ore_app as (
    select id from ores_compute_apps_tbl
    where name = 'ORE'
      and tenant_id = ores_iam_system_tenant_id_fn()
      and valid_to = ores_utility_infinity_timestamp_fn()
),
inserted_version as (
    insert into ores_compute_app_versions_tbl (
        id,
        tenant_id,
        version,
        app_id,
        wrapper_version,
        engine_version,
        package_uri,
        min_ram_mb,
        modified_by,
        performed_by,
        change_reason_code,
        change_commentary,
        valid_from,
        valid_to
    )
    select
        gen_random_uuid(),
        ores_iam_system_tenant_id_fn(),
        1,
        ore_app.id,
        '1.0.0',
        '9.0.0',
        'https://ore.example.com/packages/ore-9.0.0-linux-x86_64.tar.gz',
        512,
        'system',
        'system',
        'system.new_record',
        '',
        current_timestamp,
        ores_utility_infinity_timestamp_fn()
    from ore_app
    where not exists (
        select 1 from ores_compute_app_versions_tbl av
        join ore_app on av.app_id = ore_app.id
        where av.engine_version = '9.0.0'
          and av.wrapper_version = '1.0.0'
          and av.tenant_id = ores_iam_system_tenant_id_fn()
          and av.valid_to = ores_utility_infinity_timestamp_fn()
    )
    returning id
),
app_version_id as (
    select id from inserted_version
    union all
    select av.id
    from ores_compute_app_versions_tbl av
    join ore_app on av.app_id = ore_app.id
    where av.engine_version = '9.0.0'
      and av.wrapper_version = '1.0.0'
      and av.tenant_id = ores_iam_system_tenant_id_fn()
      and av.valid_to = ores_utility_infinity_timestamp_fn()
    limit 1
)
-- -------------------------------------------------------------------------
-- ORE App Version Platform (linux-x86_64)
-- -------------------------------------------------------------------------
insert into ores_compute_app_version_platforms_tbl (app_version_id, platform_code)
select id, 'linux-x86_64' from app_version_id
on conflict (app_version_id, platform_code) do nothing;
