def load(name):
    with open(os.path.join(SCRIPT_DIR, f"{name}.json")) as f:
        return json.load(f)


def sql_str(v):
    if v is None:
        return "null"
    return "'" + str(v).replace("'", "''") + "'"


def sql_bool(v):
    return "true" if v else "false"


def write(filename, content):
    os.makedirs(OUTPUT_DIR, exist_ok=True)
    path = os.path.join(OUTPUT_DIR, filename)
    with open(path, "w") as f:
        f.write(content)
    print(f"Wrote {path}")


def dataset_upsert(code, subject_area, name, description, artefact_type):
    return f"""    PERFORM ores_dq_datasets_upsert_fn(ores_utility_system_tenant_id_fn(),
        '{code}',
        'Acme Bank',
        '{subject_area}',
        'Reference Data',
        'NONE',
        'Primary',
        'Synthetic',
        'Raw',
        'OreStudio Code Generation Methodology',
        '{name}',
        '{description}',
        'ACMEBANK',
        'Acme Bank generated data',
        current_date,
        'Internal Use Only',
        '{artefact_type}'
    );
"""


